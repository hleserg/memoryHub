_CREATE_PARENT_SQL = """
CREATE TABLE IF NOT EXISTS eval.benchmark_runs (
    id              UUID NOT NULL DEFAULT gen_random_uuid(),
    benchmark_id    TEXT NOT NULL,
    benchmark_kind  TEXT NOT NULL,
    variant         TEXT NOT NULL DEFAULT 'default',
    git_sha         TEXT NOT NULL,
    model_llm       TEXT NOT NULL,
    model_embed     TEXT NOT NULL,
    started_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    ended_at        TIMESTAMPTZ,
    status          eval.run_status NOT NULL DEFAULT 'pending',
    verdict         eval.verdict,
    metrics_json    JSONB NOT NULL DEFAULT '{}'::jsonb,
    notes           TEXT,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    PRIMARY KEY (id, started_at),
    CHECK (ended_at IS NULL OR ended_at >= started_at)
)
PARTITION BY RANGE (started_at);

ALTER TABLE eval.benchmark_runs OWNER TO atman_eval_owner;

COMMENT ON TABLE eval.benchmark_runs IS
    'Central registry of every benchmark execution. Partitioned by month on '
    'started_at. PRIMARY KEY (id, started_at) is a partitioning requirement; '
    'id alone is globally unique by UUIDv4.';
"""


_CREATE_INDEXES_SQL = """
CREATE INDEX IF NOT EXISTS idx_benchmark_runs_benchmark_started
    ON eval.benchmark_runs (benchmark_id, started_at DESC);
CREATE INDEX IF NOT EXISTS idx_benchmark_runs_git_sha
    ON eval.benchmark_runs (git_sha);
CREATE INDEX IF NOT EXISTS idx_benchmark_runs_metrics_gin
    ON eval.benchmark_runs USING GIN (metrics_json);
"""


_GRANTS_SQL = """
GRANT SELECT, INSERT, UPDATE ON eval.benchmark_runs TO atman_eval_writer;
GRANT SELECT ON eval.benchmark_runs TO atman_eval_reader;
"""


_DROP_PARENT_SQL = "DROP TABLE IF EXISTS eval.benchmark_runs CASCADE;"


def _current_month_partition_sql() -> str:
    """Return SQL creating a partition for the current calendar month.

    Bounds are ``[YYYY-MM-01 00:00 UTC, next-month-01 00:00 UTC)``. Inclusive
    of the start, exclusive of the end — aligned with PostgreSQL's
    ``FOR VALUES FROM ... TO ...`` semantics.
    """
    now = datetime.now(UTC)
    year = now.year
    month = now.month
    next_year = year + (1 if month == 12 else 0)
    next_month = 1 if month == 12 else month + 1
    partition_name = f"benchmark_runs_{year:04d}_{month:02d}"
    start_bound = f"{year:04d}-{month:02d}-01 00:00:00+00"
    end_bound = f"{next_year:04d}-{next_month:02d}-01 00:00:00+00"
    return (
        f"CREATE TABLE IF NOT EXISTS eval.{partition_name} "
        f"PARTITION OF eval.benchmark_runs "
        f"FOR VALUES FROM ('{start_bound}') TO ('{end_bound}');\n"
        f"ALTER TABLE eval.{partition_name} OWNER TO atman_eval_owner;"
    )


def upgrade() -> None:
    """Create the partitioned table, the current-month partition, indexes and grants.

    Order matters: the parent table must exist before the child partition; the
    child partition must exist before grants are issued (PG copies the parent's
    grants automatically, but explicit grants are kept for auditability).
    """
    op.execute(_CREATE_PARENT_SQL)
    op.execute(_current_month_partition_sql())
    op.execute(_CREATE_INDEXES_SQL)
    op.execute(_GRANTS_SQL)


def downgrade() -> None:
    """Drop the partitioned table CASCADE, which removes all partitions."""
    op.execute(_DROP_PARENT_SQL)
