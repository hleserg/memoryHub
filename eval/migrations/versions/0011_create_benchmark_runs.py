"""Create eval.benchmark_runs partitioned table.

Revision ID: 0011_create_benchmark_runs
Revises: 0010_create_eval_schema
Create Date: 2026-05-10

Second migration of epic E0 (#189). Creates the central registry of every
benchmark execution: ``eval.benchmark_runs``, partitioned by
``RANGE (started_at)`` with one initial monthly partition for the current
month. Three indexes are created on the partitioned root and inherited by
every partition: ``(benchmark_id, started_at DESC)``, ``(git_sha)``, and a
GIN index on the ``metrics_json`` JSONB column.

Partition key requirement: PostgreSQL requires the partition key to be part
of every UNIQUE / PRIMARY KEY constraint on a partitioned table. We therefore
declare ``PRIMARY KEY (id, started_at)``. ``id`` is generated by
``gen_random_uuid()`` (Postgres 16 builtin) so it is globally unique with
overwhelming probability; the composite PK is purely a partitioning
constraint. Supporting tables in #218 reference ``(run_id, run_started_at)``
to satisfy the FK requirement.

Production isolation: this migration ONLY touches the ``eval`` schema. It
does not depend on any object in ``public.*``. See
``docs/architecture/PROD_EVAL_BOUNDARY.md``.
"""

